#include "../../includes/libft.h"

void	ft_strclr(char *s)
{
	int	len_s;

	if (!s)
		return ;
	len_s = ft_strlen(s);
	while (len_s >= 0)
	{
		s[len_s] = '\0';
		len_s--;
	}
}
