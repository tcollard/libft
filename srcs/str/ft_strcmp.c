#include "../../includes/libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int	i;

	i = 0;
	if ((!s1 && s2) || (!s2 && s1))
		return (-1);
	if (!s1 && !s2)
		return (0);
	while (s1[i] == s2[i] && s1[i] != '\0')
		i++;
	return ((unsigned char)s1[i] - (unsigned char)s2[i]);
}
