#include "../../includes/libft.h"

void	ft_memdel2(char ***ap)
{
	size_t	x;
	char	**a;

	a = *ap;
	x = 0;
	if (!ap || !(*ap))
		return ;
	while (a[x])
	{
		ft_memdel0(&a[x]);
		x += 1;
	}
	free(*ap);
	*ap = 0;
}
