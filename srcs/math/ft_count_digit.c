#include "../../includes/libft.h"

int	ft_count_digit(int nb)
{
	int	i;

	i = 0;
	if (nb == 0)
		return (1);
	while (nb > 0)
	{
		nb = nb / 10;
		i += 1;
	}
	return (i);
}
